// Increament and decrement operators in dart

void main() {

  // Declaring some values
  int num1 = 0;
  int num2 = 0;

 // Pre increment
  num2 = ++num1;
  print("The value of num2 is $num2");

  // Reset value to 0
  num1 = 0;
  num2 = 0;

  // Post increment
  num2 = num1++;
  print("The value of num2 is $num2");

}
